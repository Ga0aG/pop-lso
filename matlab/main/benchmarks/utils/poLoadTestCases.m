function X = poLoadTestCases(d)
% Load test cases ranging from 1-d to 7-d.
%
% Input ->
%   d : function dimension, specified as a positive integer scalar.
% Output <-
%   X : test cases, specified as a matrix whose row and column size
%       equal function dimension (F) and population size (P), respectively.
switch d
    case 1
        X = [-2 -1 0 1 2];
    case 2
        X = [...
            -2 -1 0 1 2; ...
             2  1 0 1 2];
    case 3
        X = [...
            -2 -1 0  1  2; ...
             2  1 0  1  2; ...
             2  1 0 -1 -2];
    case 4
        X = [...
            0 1 -1  1 1  1 -4; ...
            0 1 -1 -1 2 -2  3; ...
            0 1 -1  1 3  3  2; ...
            0 1 -1 -1 4 -4 -1];
    case 5
        X = [...
            0 1 -1  1 1  1 -5; ...
            0 1 -1 -1 2 -2  4; ...
            0 1 -1  1 3  3  3; ...
            0 1 -1 -1 4 -4  2; ...
            0 1 -1  1 5  5 -1];
    case 6
        X = [...
            0 1 -1  1 1  1 -6; ...
            0 1 -1 -1 2 -2  5; ...
            0 1 -1  1 3  3  4; ...
            0 1 -1 -1 4 -4  3; ...
            0 1 -1  1 5  5  2; ...
            0 1 -1  1 6 -6 -1];
    case 7
        X = [...
            0 1 -1  1 1  1 -7 0; ...
            0 1 -1 -1 2 -2  6 1; ...
            0 1 -1  1 3  3  5 2; ...
            0 1 -1 -1 4 -4  4 3; ...
            0 1 -1  1 5  5  3 4; ...
            0 1 -1  1 6 -6  2 5; ...
            0 1 -1 -1 7  7 -1 6];
    otherwise
        error(['the input should be a positive integer scalar ' ...
            'less than or equal to 7.']);
end
end
